#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		l;
	char	*dest;

	l = ft_strlen(s1);
	dest = (char *) malloc((l * sizeof(char)) + 1);
	if (!dest)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
